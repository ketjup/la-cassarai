     /**
     * @param array $value
     * @return mixed
     */
    public function getBetween(array $value)
    {
        return $this->createQueryBuilder('r')
            ->select('IDENTITY(r.room)')
            ->where('r.start BETWEEN :begin AND :end')
            ->orWhere('r.end BETWEEN :begin AND :end')
            ->setParameter('begin', $value[0])
            ->setParameter('end', $value[1])
            ->getQuery()
            ->getResult()
            ;
    }

